                                                                    <h3 id="modifying-header-display-and-behavior" class="style-scope page-guide">Modifying header display and behavior</h3><p class="style-scope page-guide">Use the <code class="style-scope page-guide">mode</code> attribute of <code class="style-scope page-guide">paper-header-panel</code> to control how the
                                                                        header displays and responds to scrolling. The list below describes
                                                                        the different valid values for <code class="style-scope page-guide">mode</code>. See the link below for a
                                                                        demonstration of all modes.</p>
                                                                    <ul class="style-scope page-guide">
                                                                        <li class="style-scope page-guide"><code class="style-scope page-guide">standard</code>: The header appears at a higher level than the content area,
                                                                            with a drop shadow. Content scrolls under the header.</li>
                                                                        <li class="style-scope page-guide"><code class="style-scope page-guide">seamed</code>: The header appears at the same level as the content area,
                                                                            with a seam between the two (no drop shadow). Content scrolls under the header.</li>
                                                                        <li class="style-scope page-guide"><code class="style-scope page-guide">waterfall</code>: The header initially presents as seamed. When content scrolls
                                                                            under the header, the header raises up and casts a drop shadow (as in
                                                                            standard mode).</li>
                                                                        <li class="style-scope page-guide"><code class="style-scope page-guide">waterfall-tall</code>: Like waterfall, except that the toolbar starts off
                                                                            tall (3x standard height) and condenses to a standard-height
                                                                            toolbar as the user scrolls. In this mode, <code class="style-scope page-guide">paper-header-panel</code> controls
                                                                            the height of the toolbar, so you should not set it yourself (via
                                                                            <code class="style-scope page-guide">medium-tall</code> or <code class="style-scope page-guide">tall</code>).</li>
                                                                        <li class="style-scope page-guide"><code class="style-scope page-guide">scroll</code>: The header is seamed with the content and scrolls with the content.</li>
                                                                        <li class="style-scope page-guide"><code class="style-scope page-guide">cover</code>: The content scrolls over the header. This mode is designed to
                                                                            be used with narrow content (for example cards).</li>
